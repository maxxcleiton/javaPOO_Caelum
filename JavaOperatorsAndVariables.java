class JavaOperatorsAndVariables{
    public static void main(String[]
    args) {
        // I want a name:
        String name = "Max";
        System.out.println(name);

        // Age:
        int birthdayYear = 1998;
        int currentlyYear = 2023;
        int maxAge;

        maxAge = currentlyYear - birthdayYear;

        System.out.println("Max's age is " + maxAge);
        
        System.out.println("");
        System.out.println("");
    }
}